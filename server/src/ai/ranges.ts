function buildRange(tightness: number): Map<RangeKey, number> {
  const map = new Map<RangeKey, number>();
  for (let i = 0; i < 13; i++) {
    for (let j = 0; j < 13; j++) {
      const suited = i < j;
      const r1 = RANKS[suited ? j : i];
      const r2 = RANKS[suited ? i : j];
      const base = suited ? BASE_SUITED[i][j] : BASE_OFFSUIT[i][j];
      map.set(key(r1, r2, suited), Math.max(0, Math.min(1, +(base * tightness).toFixed(2))));
    }
  }
  // Pairs
  for (let i = 0; i < 13; i++) {
    map.set(key(RANKS[i], RANKS[i], false), Math.max(0, Math.min(1, +(BASE_SUITED[i][i] * tightness).toFixed(2))));
  }
  return map;
}

export const RANGES = {
  tight: buildRange(0.7),
  balanced: buildRange(1.0),
  loose: buildRange(1.15),
};

export function preflopStrength(rank1: string, rank2: string, suited: boolean, range: Map<RangeKey, number>): number {
  return range.get(key(rank1, rank2, suited)) ?? 0.3;
}
