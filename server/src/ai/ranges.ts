type RangeKey = string; // "AKs", "AA", "72o"

function key(rank1: string, rank2: string, suited: boolean): RangeKey {
  if (suited) return `${rank1}${rank2}s`;
  return rank1 === rank2 ? `${rank1}${rank1}` : `${rank1}${rank2}o`;
}

